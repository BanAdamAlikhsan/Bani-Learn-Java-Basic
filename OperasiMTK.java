public class OperasiMTK {
    public static void main(String[] args){

        Integer a= 1000;
        Integer b= 100;

        //BEBERAPA OPRATOR MTK DI JAVA
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a / b);
        System.out.println(a % b);



        int c= 10;

        c +=10;
        System.out.println(c);

        c -=5;
        System.out.println(c);

        c /=5;
        System.out.println(c);

        c %=5;
        System.out.println(c);

    }
}
